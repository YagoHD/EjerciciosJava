
public class VariablesNumericas {
    public static void main(String[] args) {
        int x=71, y=32;
        
        //SUMA
        int resultado = x+y;
        System.out.println("La suma de x: "+ x + " & y: " +y+" es de: " + resultado);
    
        //RESTA
        resultado = x-y;
        System.out.println("La resta de x: "+ x + " & y: " +y+" es de: " + resultado);

        //MULTIPLICACION
        System.out.println("La multiplicacion de x: "+ x + " & y: " +y+" es de: " + (x*y));

        //DIVISION
        System.out.println("La division de x: "+ x + " & y: " +y+" es de: " + (x/y));

        //MODULO
        System.out.println("El modulo de x: "+ x + " & y: " +y+" es de: " + (x%y));
    }
    
}
